CREATE TABLE VENDOR (
  VD_NO CHAR(10) PRIMARY KEY NOT NULL,
  VD_EMAIL VARCHAR2(30 CHAR) NOT NULL,
  VD_PWD VARCHAR2(20 CHAR) NOT NULL,
  VD_NAME VARCHAR2(10 CHAR) NOT NULL,
  VD_ID CHAR(10) NOT NULL,
  VD_BIRTH DATE,
  VD_MOBILE CHAR(10) NOT NULL,
  VD_CGNAME VARCHAR2(20 CHAR) NOT NULL,
  VD_CGTEL VARCHAR2(10 CHAR) NOT NULL,
  VD_CGADDR VARCHAR2(50 CHAR) NOT NULL,
  VD_TAXID CHAR(8) NOT NULL,
  VD_ACC VARCHAR2(17 CHAR) NOT NULL,
  VD_REGDATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  VD_STAT NUMBER(1) DEFAULT 1 NOT NULL,
  VD_CGSTAT NUMBER(1) DEFAULT 1 NOT NULL,
  VD_LAT NUMBER(6,4),
  VD_LON NUMBER(7,4)
