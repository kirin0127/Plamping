CREATE TABLE CAMP_TYPE (
    CT_NO CHAR(10) PRIMARY KEY NOT NULL,
    CT_NAME VARCHAR2(20 CHAR) NOT NULL
);

CREATE TABLE CAMP (
    CAMP_NO CHAR(10) PRIMARY KEY NOT NULL,
    CAMP_VDNO CHAR(10) NOT NULL,
    CAMP_NAME VARCHAR2(10 CHAR) NOT NULL,
    CAMP_CTNO CHAR(10),
    CAMP_QTY NUMBER(3) NOT NULL CHECK(CAMP_QTY > 0),
    CAMP_PRICE NUMBER(10) NOT NULL CHECK(CAMP_PRICE > 0),
    CAMP_STAT NUMBER(1) DEFAULT 0 NOT NULL,
    CAMP_PIC BLOB,
    CONSTRAINT FK_CAMP_VENDOR
    FOREIGN KEY(CAMP_VDNO) REFERENCES VENDOR(VD_NO),
    CONSTRAINT FK_CAMP_CAMPTYPE
    FOREIGN KEY(CAMP_CTNO) REFERENCES CAMP_TYPE(CT_NO)
);

CREATE TABLE CAMP_AVAIL (
    CA_CAMPNO CHAR(10) NOT NULL,
    CA_DATE DATE NOT NULL,
    CA_QTY NUMBER(3) NOT NULL CHECK(CA_QTY >= 0),
    CONSTRAINT CA_CAMPNO PRIMARY KEY(CA_CAMPNO, CA_DATE),
    CONSTRAINT FK_CAMPAVAIL_CAMP
    FOREIGN KEY(CA_CAMPNO) REFERENCES CAMP(CAMP_NO)
);

